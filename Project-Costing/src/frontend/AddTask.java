    private void taskBackBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_taskBackBtnActionPerformed
        // TODO add your handling code here:
        this.dispose();
        if(task != null){
            previousScreen.getVersionEndField().setText(task.getEndDate().toString());
            previousScreen.getVersionCostField().setText(String.valueOf(task.getCost()));
            previousScreen.getVersionDurationField().setText(String.valueOf(task.getDuration()));
            previousScreen.getVersionTasksField().setText(String.valueOf(version.getNumTasks()));
        }
        previousScreen.setVisible(true);
    }//GEN-LAST:event_taskBackBtnActionPerformed

    private void addTaskBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTaskBtnActionPerformed
        String name, type, startDate;
        double duration;
        try {
            name = taskNameField.getText();
            type = taskTypeField.getText();
            java.sql.Date sqlDate = new java.sql.Date(taskStartField.getDate().getTime());
            startDate = sqlDate.toString();
            System.out.println("##Test Printing##");
            System.out.println(startDate);
            System.out.println(name);
            System.out.println(type);
            duration = Double.parseDouble(taskDurationField.getText());
            String res = (String)taskResources.getSelectedItem();
            System.out.println(res);
            task = new Task(name, type, startDate, duration, res);
            version.addTask(task);
            taskNameField.setText("");
            taskTypeField.setText("");
            taskDurationField.setText("");
            taskEndField.setText("");
            taskCostField.setText("");
            taskNameField.requestFocusInWindow();
        } catch (NumberFormatException e) {
            System.out.println("Something wrong with the number " + e.getMessage());
        } catch (Exception e) {
            System.out.println("Or something else happeneing " + e.getMessage());
        }

    }//GEN-LAST:event_addTaskBtnActionPerformed

